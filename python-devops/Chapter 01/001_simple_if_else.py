# Initialize the variable `age` with a value of 16
age = 16

# Check if the age is less than 18
if age < 18:
    # Print this message if the person is a minor
    print("You are a minor!")
# Check if the age is exactly 18
elif age == 18:
    # Print this message if the person just became an adult
    print("You just became an adult!")
# If age is greater than 18
else:
    # Print this message if the person is an adult
    print("You are an adult!")
